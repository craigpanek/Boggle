/**
 * A class that represents a pair of ints and a boolean flag.
 * This class can be used to return multiple primitives from a method.
 * @author Craig Panek
 * Date: 6-19-2014
 */
public class MyPair
{
    public int first;
    public int second;
    public boolean flag;

    public MyPair(int first, int second, boolean flag)
    {
        this.first = first;
        this.second = second;
        this.flag = flag;
    }
}
